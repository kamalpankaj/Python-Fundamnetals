# Numeric Data Type
# Integer Data Type
print("Integer Data Type")
print('''Integer is whole number, posivive or negative, 
without decimal of unlimited length''')
A=1
B=123456789
C=-987654321
print(type(A))
print(type(B))
print(type(C))

# Float Data Type
print("Float Data Type")
print('''float, or floating point number is a number, positive or negative
containing one or more decimals.''')
A=1.2
B=1.00
C=-1.002
print(type(A))
print(type(B))
print(type(C))

# Complex Data Type
print("Complex Data Type")
print('''Complex Number are written with a "j" as the imaginary part.''')
A=3j+5j
B=6j
C=-8j
print(type(A))
print(type(B))
print(type(C))

